WHAT IS INHERITANCE?

    #INHERITANCE is a way for a class to obtain all methods from
        #another class
    
    #INHERITANCE avoids duplicate code across multiple classes

    #The class inherited from is called the superclass or parent class

    #The class that inherits is called the Subclass or child class

    #Ruby only supports single inheritance. A class can only inherit 
        #from one other class

    #The subclass is a type of the superclass. This is call an "is-a"
        #relationship

    #The relationship does not apply in reverse.

    #Thus, the subclass can be called a subset of the superclass

    #A car is a vehicle but a Vehicle is not guearanteed to be a car.

    #Sublasses are followed by a < symbol and the name of the superclass
        #Ex)  Class Car < Vehicle
    
    